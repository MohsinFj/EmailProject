    public AddClientNew() {
        initComponents();
        
        try {
            File fileIp = new File(System.getProperty("user.dir") + "\\src\\IP\\ip.txt");
            byte[] readAllBytes = java.nio.file.Files.readAllBytes(Paths.get(fileIp.getAbsolutePath()));
            String ip = new String(readAllBytes).trim();
            this.ip = ip;
        } catch (IOException ex) {
            Logger.getLogger(clientFormNew.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
//            ip = InetAddress.getLocalHost();
            Socket other = new Socket(ip, 9999);
            
            InputStream is = other.getInputStream();
            OutputStream out = other.getOutputStream();
            
            ObjectOutputStream writeSource = new ObjectOutputStream(out);
            writeSource.writeObject(new Object[]{"getDepartmentName"});
            
            ObjectInputStream readSource = new ObjectInputStream(is);
            ArrayList<String> info = (ArrayList<String>)readSource.readObject();
            
            if(!info.isEmpty())
            {
                cmbDepartment.addItem("");
                for (Object info1 : info) {
                    cmbDepartment.addItem(info1.toString());
                }
            }
            
            is.close();
            out.close();
            writeSource.close();
            readSource.close();
            other.close();
            
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(clientFormNew.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }

   

