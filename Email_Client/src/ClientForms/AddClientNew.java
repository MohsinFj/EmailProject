    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtClientName.getText();
        String email = txtClientEmail.getText();
        String password = txtClientPassword.getText();
        String department = cmbDepartment.getSelectedItem().toString();
        if(!name.isEmpty() && !email.isEmpty() && !password.isEmpty() && !department.isEmpty())
        {
            try {
                Socket other = new Socket(ip, 9999);

                InputStream is = other.getInputStream();
                OutputStream out = other.getOutputStream();

                ObjectOutputStream writeSource = new ObjectOutputStream(out);
                writeSource.writeObject(new Object[]{"addNewClient",name,email,password,department});

                ObjectInputStream readSource = new ObjectInputStream(is);
                String addResult = readSource.readUTF();
                JOptionPane.showMessageDialog(null, addResult);
                
                txtClientName.setText("");
                txtClientEmail.setText("");
                txtClientPassword.setText("");
                cmbDepartment.setSelectedIndex(0);
                txtClientName.requestFocus();
            } catch (IOException ex) {
                Logger.getLogger(AddClientNew.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "الرجاء ملئ الحقول");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

